package Inheritence;

public class Animal {
	
	private String name;
	private int leg;
	private boolean teil;
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public int getLeg() {
		return leg;
	}
	public void setLeg(int leg) {
		this.leg = leg;
	}
	public boolean isTeil() {
		return teil;
	}
	public void setTeil(boolean teil) {
		this.teil = teil;
	}
	
	

}
